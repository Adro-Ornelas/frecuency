package POJO;

public class Artista {

    int id;
    String nombreArt;
    String nombreReal;
    String apellidoP;
    String apellidoM;
    String telefonoCont;
    String fecha_show;
    String ciudadShow;
    String horaInicio;
    String horaFinal;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }
    public String getNombreArt() {
        return nombreArt;
    }

    public void setNombreArt(String nombreArt) {
        this.nombreArt = nombreArt;
    }

    public String getNombreReal() {
        return nombreReal;
    }

    public void setNombreReal(String nombreReal) {
        this.nombreReal = nombreReal;
    }

    public String getApellidoP() {
        return apellidoP;
    }

    public void setApellidoP(String apellidoP) {
        this.apellidoP = apellidoP;
    }

    public String getApellidoM() {
        return apellidoM;
    }

    public void setApellidoM(String apellidoM) {
        this.apellidoM = apellidoM;
    }

    public String getTelefonoCont() {
        return telefonoCont;
    }

    public void setTelefonoCont(String telefonoCont) {
        this.telefonoCont = telefonoCont;
    }

    public String getFecha_show() {
        return fecha_show;
    }

    public void setFecha_show(String fecha_show) {
        this.fecha_show = fecha_show;
    }

    public String getCiudadShow() {
        return ciudadShow;
    }

    public void setCiudadShow(String ciudadShow) {
        this.ciudadShow = ciudadShow;
    }

    public String getHoraInicio() {
        return horaInicio;
    }

    public void setHoraInicio(String horaInicio) {
        this.horaInicio = horaInicio;
    }

    public String getHoraFinal() {
        return horaFinal;
    }

    public void setHoraFinal(String horaFinal) {
        this.horaFinal = horaFinal;
    }
}
